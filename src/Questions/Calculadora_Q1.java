package Questions;

public class Calculadora_Q1 {

	
	
	public static int Somar(int number_1, int number_2) {
		
		return number_1 + number_2;
	}
	
	public static int Subtrair(int number_1 , int number_2) {
		return number_1 - number_2;
	}
	
	public static int multiplicar (int number_1 , int number_2) {
		return number_1 * number_2;
	}
	
	public static int dividir (int number_1, int number_2) {
		if(number_2 == 0) {
			return 0;
		}
		return number_1 / number_2;
	}
	
	
	
	
	
	
	
	
	
	
	
}
